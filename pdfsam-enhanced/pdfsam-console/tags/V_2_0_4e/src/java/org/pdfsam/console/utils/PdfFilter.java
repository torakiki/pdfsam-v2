package org.pdfsam.console.utils;

import java.io.File;
import java.io.FileFilter;
/**
 * Pdf filter
 * @author Andrea Vacondio
 */
public class PdfFilter implements FileFilter {

	private static String EXTENSION = "pdf";
	
	public boolean accept(File arg0) {
		boolean retVal = false;
		if (arg0!=null && !arg0.isDirectory()){	       	
		   String extension = getExtension(arg0);
		   retVal = EXTENSION.equals(extension);		       
		}
		return retVal;
	}

	/**
     * Get the extension of a file.
     */  
    public String getExtension(File f) {
        String ext = null;
        String s = f.getName();
        int i = s.lastIndexOf('.');

        if (i > 0 &&  i < s.length() - 1) {
            ext = s.substring(i+1).toLowerCase();
        }
        return ext;
    }
}
